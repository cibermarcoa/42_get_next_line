char	*ft_setbuffer(int fd, char *buffer)
{
	char	*aux_buffer;
	ssize_t	read_value;

	aux_buffer = (char *)malloc(sizeof(char) * (BUFFER_SIZE + 1));
	if (!aux_buffer)
		return (0);
	read_value = 1;
	while (read_value != 0)
	{
		read_value = read(fd, aux_buffer, BUFFER_SIZE);
		if (read_value == -1)
		{
			free(aux_buffer);
			return (0);
		}
		aux_buffer[read_value] = '\0';
		buffer = ft_strjoin(aux_buffer, aux_buffer);
	}
	
	free(aux_buffer);
	return (buffer);
}

